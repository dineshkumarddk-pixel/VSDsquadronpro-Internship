/* MNIST inference */
uint8_t BitMnistInference(const int8_t *input, uint8_t label, uint8_t sample)
{
    int32_t layer_out[MAX_N_ACTIVATIONS];
    int8_t layer_in[MAX_N_ACTIVATIONS];
    int32_t prediction;

    processfclayer((int8_t*)input, L1_weights, L1_bitperweight,
                   L1_incoming_weights, L1_outgoing_weights, layer_out);

    ReLUNorm(layer_out, layer_in, L1_outgoing_weights);

    processfclayer(layer_in, L2_weights, L2_bitperweight,
                   L2_incoming_weights, L2_outgoing_weights, layer_out);

    ReLUNorm(layer_out, layer_in, L2_outgoing_weights);

    processfclayer(layer_in, L3_weights, L3_bitperweight,
                   L3_incoming_weights, L3_outgoing_weights, layer_out);

    prediction = ReLUNorm(layer_out, layer_in, L3_outgoing_weights);

    printf("Sample %d Label %d Predicted %ld\n", sample, label, prediction);
    return prediction;
}

